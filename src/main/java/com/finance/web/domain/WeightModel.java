package com.finance.web.domain;

public class WeightModel {
	
	private float kilogram;
	
	private float gram;
	
	private float milligram;
	
	public WeightModel() {
		
	}

	public float getKilogram() {
		return kilogram;
	}

	public void setKilogram(float kilogram) {
		this.kilogram = kilogram;
	}

	public float getGram() {
		return gram;
	}

	public void setGram(float gram) {
		this.gram = gram;
	}

	public float getMilligram() {
		return milligram;
	}

	public void setMilligram(float milligram) {
		this.milligram = milligram;
	}

}
